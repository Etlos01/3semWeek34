package model;

/**
 *
 * @author Nyxis
 */
public class AnimalNoDB {
    String type;
    String sound;

    public AnimalNoDB(String type, String sound) {
        this.type = type;
        this.sound = sound;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSound() {
        return sound;
    }

    public void setSound(String sound) {
        this.sound = sound;
    }
    
}
